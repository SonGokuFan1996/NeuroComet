def xml_escape(value: str) -> str:
    # Order matters – escape ampersands first, then quotes/apostrophes.
    return (
        value.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace("'", "\\'")
    )


def inject(locale_dir: Path, entries: dict[str, str]) -> None:
    path = locale_dir / "strings.xml"
    if not path.exists():
        return

    text = path.read_text(encoding="utf-8")

    for key, value in entries.items():
        escaped = xml_escape(value)
        new_line = f'    <string name="{key}">{escaped}</string>'
        pattern = re.compile(
            rf'^[ \t]*<string\s+name="{re.escape(key)}"[^>]*>.*?</string>\s*$',
            re.MULTILINE | re.DOTALL,
        )
        if pattern.search(text):
            text = pattern.sub(new_line, text, count=1)
        else:
            text = text.replace("</resources>", f"{new_line}\n</resources>", 1)

    path.write_text(text, encoding="utf-8")


def main() -> None:
    print("Injecting pill and settings translations…")

    # 1) Apply curated translations for the locales we cover directly.
    for locale, entries in TRANSLATIONS.items():
        locale_dir = RES / f"values-{locale}"
        if locale_dir.exists():
            inject(locale_dir, entries)
            print(f"  ok: values-{locale}")

    # 2) For every other values-* directory, fill in missing keys with the
    #    English fallback so resource linting stays green.  Existing
    #    translations are preserved – the regex only rewrites exact matches.
    for locale_dir in sorted(RES.glob("values-*")):
        name = locale_dir.name
        if not (locale_dir / "strings.xml").exists():
            continue
        # Skip non-locale qualifiers
        if name in {"values-night", "values-notnight"}:
            continue
        stripped = name.removeprefix("values-").split("-")[0]
        if stripped in TRANSLATIONS:
            continue  # already handled
        # Only inject keys that are missing (don't overwrite any prior work)
        text = (locale_dir / "strings.xml").read_text(encoding="utf-8")
        missing = {k: v for k, v in EN_FALLBACK.items()
                   if re.search(rf'name="{re.escape(k)}"', text) is None}
        if missing:
            inject(locale_dir, missing)
            print(f"  fallback: {name} ({len(missing)} keys)")

    print("Done.")


if __name__ == "__main__":
    main()

