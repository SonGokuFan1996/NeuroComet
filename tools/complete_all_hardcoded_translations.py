def escape(text):
    """Properly escape XML special characters and apostrophes."""
    res = text.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")
    # Handle apostrophes - escape only if not already escaped
    if "'" in res and "\\'" not in res:
        res = res.replace("'", "\\'")
    return res


def add_translation(content, key, value):
    """Add a new translation to the strings.xml content if it doesn't exist."""
    # Check if key already exists
    pattern = rf'<string\s+name="{re.escape(key)}"'
    if re.search(pattern, content):
        return content, False  # Key exists, don't add

    # Find the closing </resources> tag and add before it
    escaped_value = escape(value)
    new_entry = f'    <string name="{key}">{escaped_value}</string>\n'

    if '</resources>' in content:
        content = content.replace('</resources>', new_entry + '</resources>')
        return content, True
    return content, False


def apply_translations(priority_name, translations_dict):
    """Apply translations from a priority dictionary."""
    total_added = 0

    for lang, translations in translations_dict.items():
        xml_file = RES_DIR / f"values-{lang}" / "strings.xml"
        if not xml_file.exists():
            print(f"  ⚠️ {lang}: File not found, skipping")
            continue

        content = xml_file.read_text(encoding='utf-8')
        added_in_file = 0

        for key, val in translations.items():
            content, added = add_translation(content, key, val)
            if added:
                added_in_file += 1

        if added_in_file > 0:
            xml_file.write_text(content, encoding='utf-8')
            print(f"  ✓ {lang}: Added {added_in_file} strings")
            total_added += added_in_file
        else:
            print(f"  - {lang}: All strings already present")

    return total_added


