def parse(p: Path) -> dict[str, str]:
    out: dict[str, str] = {}
    if not p.exists():
        return out
    content = p.read_text(encoding="utf-8", errors="replace")
    for m in re.finditer(r'<string\s+name="([^"]+)"[^>]*>(.*?)</string>', content, flags=re.DOTALL):
        out[m.group(1)] = m.group(2)
    return out


