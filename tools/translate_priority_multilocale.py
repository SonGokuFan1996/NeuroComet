# Map locale dir suffix → translation dict.
LOCALE_MAP: dict[str, dict[str, str]] = {
    "es": ES, "fr": FR, "de": DE, "pt": PT, "it": IT, "nl": NL,
    "ja": JA, "ko": KO, "zh": ZH, "ru": RU, "tr": TR, "pl": PL,
    "sv": SV, "nb": NB, "da": DA, "fi": FI, "cs": CS, "hu": HU,
    "ro": RO, "el": EL, "uk": UK, "iw": IW, "hi": HI_EXTRA,
    "ur": UR, "in": ID, "ms": MS, "vi": VI, "th": TH, "is": IS,
}


def inject(loc_dir: Path, translations: dict[str, str], en: dict[str, str]) -> tuple[int, int]:
    """Inject curated translations into a locale's strings.xml.

    Returns (added, replaced) counts.
    """
    sx = loc_dir / "strings.xml"
    if not sx.exists():
        return (0, 0)
    existing = parse(sx)
    content = sx.read_text(encoding="utf-8")

    added = 0
    replaced = 0
    lines_to_add: list[str] = []

    for key, value in translations.items():
        if key not in en:
            continue
        if key not in existing:
            lines_to_add.append(f'    <string name="{key}">{value}</string>')
            added += 1
        elif existing[key] == en[key]:
            # Replace stale English fallback entry
            pattern = re.compile(
                r'(<string\s+name="' + re.escape(key) + r'"[^>]*>)(.*?)(</string>)',
                flags=re.DOTALL,
            )
            content = pattern.sub(lambda m: m.group(1) + value + m.group(3), content, count=1)
            replaced += 1

    if lines_to_add:
        block = "\n    <!-- Curated priority translations -->\n" + "\n".join(lines_to_add) + "\n"
        content = content.replace("</resources>", block + "</resources>", 1)

    if added or replaced:
        sx.write_text(content, encoding="utf-8", newline="\n")
    return (added, replaced)


def main() -> int:
    en = parse(EN)
    total_added = 0
    total_replaced = 0
    for loc_dir in sorted(RES.glob("values-*")):
        suffix = loc_dir.name.removeprefix("values-")
        if suffix in LOCALE_MAP:
            trans = LOCALE_MAP[suffix]
            a, r = inject(loc_dir, trans, en)
            total_added += a
            total_replaced += r
            print(f"  {loc_dir.name:20s}  +{a:4d} strings, ~{r:4d} replaced")
    print(f"\nTotal: +{total_added} added, ~{total_replaced} replaced across locales.")
    return 0


