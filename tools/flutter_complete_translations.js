// Parse the dart file and extract all language blocks
function parseLangBlocks(content) {
  const blocks = {};
  // Match each language block: 'xx': { ... },
  const langRegex = /^\s*'([a-z]{2})':\s*\{/gm;
  let match;
  while ((match = langRegex.exec(content)) !== null) {
    const langCode = match[1];
    const startIdx = match.index;
    // Find the matching closing },
    let braceCount = 0;
    let endIdx = -1;
    for (let i = content.indexOf('{', startIdx); i < content.length; i++) {
      if (content[i] === '{') braceCount++;
      if (content[i] === '}') braceCount--;
      if (braceCount === 0) {
        endIdx = i;
        break;
      }
    }
    if (endIdx === -1) continue;

    const blockContent = content.substring(content.indexOf('{', startIdx), endIdx + 1);
    // Extract key-value pairs
    const kvRegex = /'([a-zA-Z0-9_]+)':\s*'((?:[^'\\]|\\.)*)'/g;
    const keys = {};
    let kvMatch;
    while ((kvMatch = kvRegex.exec(blockContent)) !== null) {
      keys[kvMatch[1]] = kvMatch[2];
    }
    blocks[langCode] = { keys, startIdx, endIdx, blockContent };
  }
  return blocks;
}

