// Apply translations - insert missing keys into each language block
function applyTranslations(content, blocks, translations) {
  const enKeys = Object.keys(blocks['en'].keys);
  let offset = 0;
  let newContent = content;

  const langCodes = Object.keys(translations);

  // Sort by position in file (ascending) so offset tracking works
  langCodes.sort((a, b) => {
    const posA = blocks[a] ? blocks[a].endIdx : Infinity;
    const posB = blocks[b] ? blocks[b].endIdx : Infinity;
    return posA - posB;
  });

  for (const lang of langCodes) {
    if (!blocks[lang]) {
      console.log(`Language '${lang}' not found in file, skipping.`);
      continue;
    }

    const existingKeys = new Set(Object.keys(blocks[lang].keys));
    const newTranslations = translations[lang];
    const keysToAdd = [];

    for (const key of Object.keys(newTranslations)) {
      if (!existingKeys.has(key)) {
        keysToAdd.push(key);
      }
    }

    if (keysToAdd.length === 0) {
      console.log(`${lang}: no new keys to add.`);
      continue;
    }

    // Build the insertion string
    let insertStr = '\n';
    for (const key of keysToAdd) {
      const val = newTranslations[key];
      insertStr += `      '${key}': '${val}',\n`;
    }

    // Find the closing }, of this language block and insert before it
    const endIdx = blocks[lang].endIdx + offset;
    // Insert before the closing }
    newContent = newContent.substring(0, endIdx) + insertStr + '    ' + newContent.substring(endIdx);

    // Adjust offset - but we replaced the } so we need to be careful
    // Actually we inserted before }, so the offset increases by insertStr.length + 4 (for the extra '    ')
    offset += insertStr.length + 4;

    console.log(`${lang}: added ${keysToAdd.length} keys.`);
  }

  return newContent;
}

// Main
function main() {
  console.log('Reading app_localizations.dart...');
  let content = fs.readFileSync(DART_FILE, 'utf8');

  console.log('Parsing language blocks...');
  const blocks = parseLangBlocks(content);

  const enKeys = Object.keys(blocks['en']?.keys || {});
  console.log(`English has ${enKeys.length} keys.`);

  for (const lang of Object.keys(blocks)) {
    const langKeys = Object.keys(blocks[lang].keys);
    const missing = enKeys.filter(k => !blocks[lang].keys[k]);
    if (missing.length > 0) {
      console.log(`${lang}: ${langKeys.length}/${enKeys.length} keys (${missing.length} missing)`);
    }
  }

  console.log('\nApplying translations...');
  const translations = getTranslations();
  content = applyTranslations(content, blocks, translations);

  console.log('\nWriting updated file...');
  fs.writeFileSync(DART_FILE, content, 'utf8');

  // Re-parse and verify
  console.log('\nVerifying...');
  const newBlocks = parseLangBlocks(content);
  const newEnKeys = Object.keys(newBlocks['en']?.keys || {});
  for (const lang of Object.keys(newBlocks)) {
    const langKeys = Object.keys(newBlocks[lang].keys);
    const missing = newEnKeys.filter(k => !newBlocks[lang].keys[k]);
    if (missing.length > 0) {
      console.log(`${lang}: ${langKeys.length}/${newEnKeys.length} (still ${missing.length} missing)`);
    } else {
      console.log(`${lang}: ${langKeys.length}/${newEnKeys.length} ✓ COMPLETE`);
    }
  }

  console.log('\nDone!');
}

