# Plurals per locale for wellbeing_detox_days_chip
PLURALS: dict[str, dict[str, str]] = {
    "es": {"one": "%d día", "other": "%d días"},
    "fr": {"one": "%d jour", "other": "%d jours"},
    "de": {"one": "%d Tag", "other": "%d Tage"},
}


def xml_escape(text: str) -> str:
    return (
        text.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace("'", "\\'")
    )


def inject_string(content: str, key: str, value: str) -> str:
    """Replace or insert <string name="key">value</string> just before </resources>."""
    pattern = re.compile(
        rf'<string\s+name="{re.escape(key)}"\s*(?:translatable="[^"]*"\s*)?>.*?</string>',
        re.DOTALL,
    )
    new_line = f'<string name="{key}">{xml_escape(value)}</string>'
    if pattern.search(content):
        return pattern.sub(new_line, content)
    return content.replace("</resources>", f"    {new_line}\n</resources>")


def inject_plural(content: str, key: str, items: dict[str, str]) -> str:
    pattern = re.compile(
        rf'<plurals\s+name="{re.escape(key)}"\s*>.*?</plurals>', re.DOTALL
    )
    body_items = "\n".join(
        f'        <item quantity="{q}">{xml_escape(v)}</item>' for q, v in items.items()
    )
    new_block = f'<plurals name="{key}">\n{body_items}\n    </plurals>'
    if pattern.search(content):
        return pattern.sub(new_block, content)
    return content.replace("</resources>", f"    {new_block}\n</resources>")


def main() -> None:
    total = 0
    for lang, entries in TRANSLATIONS.items():
        path = RES / f"values-{lang}" / "strings.xml"
        if not path.exists():
            print(f"[skip] missing {path}")
            continue
        content = path.read_text(encoding="utf-8")
        for key, value in entries.items():
            content = inject_string(content, key, value)
            total += 1
        if lang in PLURALS:
            content = inject_plural(content, "wellbeing_detox_days_chip", PLURALS[lang])
        path.write_text(content, encoding="utf-8")
        print(f"[ok]   {lang}: {len(entries)} keys + plurals")
    print(f"Done. Injected {total} string entries across {len(TRANSLATIONS)} locales.")


if __name__ == "__main__":
    main()

