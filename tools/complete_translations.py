import os
import re

RES_DIR = os.path.join(os.path.dirname(__file__), '..', 'app', 'src', 'main', 'res')

def update_translations(lang_dir, translations_dict):
    """Update translations in a language file."""
    lang_file = os.path.join(RES_DIR, lang_dir, 'strings.xml')

    base_file = os.path.join(RES_DIR, 'values', 'strings.xml')
    with open(base_file, 'r', encoding='utf-8') as f:
        base_content = f.read()
    base_strings = dict(re.findall(r'<string name="([^"]+)">([^<]*)</string>', base_content))

    with open(lang_file, 'r', encoding='utf-8') as f:
        lines = f.readlines()

    updated_count = 0
    new_lines = []

    for line in lines:
        updated = False
        for name, new_text in translations_dict.items():
            pattern = rf'^(\s*<string name="{re.escape(name)}">)(.*)(<\/string>\s*)$'
            match = re.match(pattern, line)
            if match:
                current_text = match.group(2)
                if name in base_strings and current_text == base_strings[name]:
                    escaped_new = new_text.replace('&', '&amp;').replace('<', '&lt;').replace('>', '&gt;')
                    if "\\'" not in escaped_new:
                        escaped_new = escaped_new.replace("'", "\\'")
                    new_line = f'{match.group(1)}{escaped_new}{match.group(3)}'
                    new_lines.append(new_line)
                    updated_count += 1
                    updated = True
                    break
        if not updated:
            new_lines.append(line)

    with open(lang_file, 'w', encoding='utf-8', newline='') as f:
        f.writelines(new_lines)

    return updated_count

