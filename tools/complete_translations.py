def main():
    """Apply all translations."""
    print("Applying comprehensive translations to all languages...\n")

    total_updated = 0
    for lang_dir, translations in TRANSLATIONS.items():
        lang_file = os.path.join(RES_DIR, lang_dir, 'strings.xml')
        if os.path.exists(lang_file):
            count = update_translations(lang_dir, translations)
            total_updated += count
            if count > 0:
                print(f"✓ {lang_dir}: Updated {count} strings")
            else:
                print(f"  {lang_dir}: No updates needed")
        else:
            print(f"✗ {lang_dir}: File not found")

    print(f"\n✅ Total: Updated {total_updated} strings across all languages")


if __name__ == '__main__':
    main()

