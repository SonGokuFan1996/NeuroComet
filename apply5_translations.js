// Apply Flutter translations
let dartContent = fs.readFileSync(dartFile, 'utf8');

for (const [lang, trans] of Object.entries(flutterTranslations)) {
  const re = new RegExp("'" + lang + "':\\s*\\{");
  const match = re.exec(dartContent);
  if (!match) { console.log(`Flutter ${lang}: NOT FOUND`); continue; }

  let bc = 0, si = match.index + match[0].length - 1, ei = si;
  for (let i = si; i < dartContent.length; i++) {
    if (dartContent[i] === '{') bc++;
    if (dartContent[i] === '}') bc--;
    if (bc === 0) { ei = i; break; }
  }

  const block = dartContent.substring(si, ei);
  const existingKeys = new Set();
  const kr = /'([a-zA-Z_][a-zA-Z0-9_]*)'\s*:/g;
  let km;
  while ((km = kr.exec(block)) !== null) existingKeys.add(km[1]);

  const newEntries = [];
  for (const [key, value] of Object.entries(trans)) {
    if (!existingKeys.has(key)) {
      // Escape single quotes in values for Dart
      const escaped = value.replace(/'/g, "\\'");
      newEntries.push(`      '${key}': '${escaped}',`);
    }
  }

  if (newEntries.length > 0) {
    const insertText = '\n      // Additional translations\n' + newEntries.join('\n') + '\n';
    dartContent = dartContent.substring(0, ei) + insertText + dartContent.substring(ei);
  }
  console.log(`Flutter ${lang}: Added ${newEntries.length} translations`);
}

fs.writeFileSync(dartFile, dartContent, 'utf8');
console.log('\nAll done!');

